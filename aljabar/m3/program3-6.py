import numpy as np
# Matriks A
A = np.array([[1, 2], [3, 4]])
# Matriks B
B = np.array([[5, 6], [7, 8]])
# Penjumlahan matriks
C = A + B
# Perkalian matriks
D = np.dot(A, B)
# Transpose matriks
E = A.T
# Menampilkan hasil
print("Penjumlahan matriks:")
print(C)
print("\nPerkalian matriks:")
print(D)
print("\nTranspose matriks:")
print(E)
